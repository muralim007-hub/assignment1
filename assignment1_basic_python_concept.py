"""Task1: Write a Python program that does the following:
Takes two numbers as input from the user.
Performs the basic mathematical operations on these two numbers: Addition Subtraction Multiplication Division
Displays the results of each operation on the screen."""


num1=int(input("Enter a number: "))
num2=int(input("Enter another number: "))
addition=num1+num2
subtraction=(num1-num2)
multiplication=num1*num2
division=num1/num2
print("addition:",addition)
print("subtraction:",subtraction)
print("multiplication:",multiplication)
print("division:",division)



